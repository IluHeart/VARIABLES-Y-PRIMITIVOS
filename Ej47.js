const names = ["Luca", "Marco", "Vittorio", "Giovanni"];

names.pop(); // Elimina el ultimo elemento del array
console.log(names);

names.splice([3]); // .splice Elimina elemento del indice indicado
console.log(names);

names.push("Pippo"); // .push Añade elemento al final del array
console.log(names);

names.unshift("Giovanni"); // .unshift Añade elemento al principio del array
console.log(names);

